/**
 * Normalize an utterance for matching:
 * - lowercase
 * - convert curly apostrophes to straight
 * - strip punctuation except apostrophes (so "don't" stays intact)
 * - collapse whitespace
 */
export function normalizeForReverence(text: string): string {
  return text
    .toLowerCase()
    .replace(/[‘’ʼ]/g, "'")
    .replace(/[^a-z0-9'\s]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Returns the first closed-door match in the subscriber utterance, or null.
 * Phrases are also matched in an apostrophe-stripped form so "id rather not"
 * (a common STT rendering) still trips.
 */
export function detectClosedDoor(utterance: string): ReverenceMatch | null {
  if (!utterance) return null;
  const normalized = normalizeForReverence(utterance);
  const normalizedNoApostrophe = normalized.replace(/'/g, '');

  for (const phrase of CLOSED_DOOR_PHRASES) {
    const needle = phrase;
    const needleNoApostrophe = phrase.replace(/'/g, '');
    if (normalized.includes(needle)) {
      return { phrase, matchedText: needle };
    }
    if (needleNoApostrophe !== needle && normalizedNoApostrophe.includes(needleNoApostrophe)) {
      return { phrase, matchedText: needleNoApostrophe };
    }
  }
  return null;
}

