/**
 * Reverence pre-filter — P0 safety surface (Durable rule #1).
 *
 * On any closed-door signal we stop that topic/person/period permanently with
 * one gentle acknowledgment and never re-approach. This filter runs
 * DETERMINISTICALLY on every subscriber turn BEFORE the turn ever reaches
 * Claude, so a closed door stays closed even if Claude misbehaves or omits a
 * structured payload.
 *
 * Keep this simple and auditable: plain normalized-substring / regex matching
 * on explicit closed-door phrases. No model, no fuzzy scoring. Every match
 * records the phrase that triggered it so the closure is fully auditable
 * (adversarial QA ship gate).
 *
 * This is intentionally a HIGH-PRECISION list of explicit refusals. It is a
 * floor, not the whole reverence system — Claude is still instructed to honor
 * subtler closed-door cues. The deterministic floor exists so the most explicit
 * signals can never slip through.
 */

export interface ReverenceMatch {
  /** The exact closed-door phrase (canonical form) that matched. */
  phrase: string;
  /** The matched substring as it appeared in the subscriber's utterance. */
  matchedText: string;
}

